package com.innowise.salescustomeranalysis.model;

/**
 * Represents the category of a product in the store.
 * <p>
 * Categories are used to classify products for analytics, filtering, and reporting purposes.
 */
public enum Category {
  /**
   * Electronic devices and accessories.
   */
  ELECTRONICS,

  /**
   * Clothing and apparel items.
   */
  CLOTHING,

  /**
   * Printed or digital books.
   */
  BOOKS,

  /**
   * Home appliances, furniture, and decor.
   */
  HOME,

  /**
   * Beauty and personal care products.
   */
  BEAUTY,

  /**
   * Toys and games for children or adults.
   */
  TOYS
}
